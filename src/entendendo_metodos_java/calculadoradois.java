package entendendo_metodos_java;

public class calculadoradois {

    public static void soma(double a, double b) {
        double resultado = a + b;

        System.out.println("A soma de de " + a + " mais " + b + " é igual a " + resultado);

    }
    public static void subtrai(double a, double b) {
        double resultado = a - b;

        System.out.println("A subtração de " + a + " menos " + b + " é igual a " + resultado);

    }
    public static void divide(double a, double b) {
        double resultado = a / b;

        System.out.println("A divisão de " + a + " por " + b + " é igual a " + resultado);

    }
    public static void multiplica(double a, double b) {
        double resultado = a * b;

        System.out.println("A multiplicação de " + a + " com " + b + " é igual a " + resultado);

    }

}



